package org.example.lessons._20240711;

public class EvenOddCheck {
    public static void main(String[] args) {

//        Создать программу, проверяющую и сообщающую на экран,
//        является ли целое число записанное в переменную n,
//        чётным либо нечётным.
        // Создаем переменную n
        int n = 30; // Можно изменить это значение для проверки других чисел

        // Проверка на четность или нечетность
        if (n % 2 == 0) {
            System.out.println(n + " является четным числом.");
        } else {
            System.out.println(n + " является нечетным числом.");
        }
    }
}
//```
//
//Этот код выполняет следующие действия:
//        1. Создает переменную `n` и присваивает ей значение (в данном случае `15`).
//        2. Проверяет, делится ли `n` на `2` без остатка с помощью оператора `%`.
//        3. Если остаток от деления равен `0`, то число четное, иначе оно нечетное.
//4. Выводит результат на экран.
//
//Вы можете изменить значение переменной `n`, чтобы проверить другие числа.
//
//После компиляции и выполнения этого кода вы увидите следующий вывод:
//
//        ```plaintext
//15 является нечетным числом.
//```
//
//Если изменить значение `n` на, например, `8`, вывод будет следующим:
//
//        ```plaintext
//8 является четным числом.
//```
//}
