package org.example.homeworks.HWALG;

import java.util.Scanner;

public class Main {
    public static void main(String[] args) {
//        Псевдокод помогает понять алгоритм, не углубляясь в детали синтаксиса языка программирования. Вот простой псевдокод для задачи сложения трёх чисел и вывода их суммы:
//
//```
//1. Начать
//2. Ввести первое число (num1)
//3. Ввести второе число (num2)
//4. Ввести третье число (num3)
//5. Вычислить сумму (sum) = num1 + num2 + num3
//6. Вывести сумму (sum)
//7. Завершить
//```
//
//### 2. Блок-схема
//
//Блок-схема визуализирует алгоритм и его шаги:
//
//1. **Старт** – Начало программы
//2. **Ввод num1** – Ввод первого числа
//3. **Ввод num2** – Ввод второго числа
//4. **Ввод num3** – Ввод третьего числа
//5. **Сложение** – Вычисление суммы: `sum = num1 + num2 + num3`
//6. **Вывод sum** – Вывод суммы на экран
//7. **Стоп** – Конец программы

        // Создание объекта Scanner для ввода данных
        Scanner scanner = new Scanner(System.in);

        // Ввод трёх чисел
        System.out.print("Введите первое число: ");
        int num1 = scanner.nextInt();

        System.out.print("Введите второе число: ");
        int num2 = scanner.nextInt();

        System.out.print("Введите третье число: ");
        int num3 = scanner.nextInt();

        // Вычисление суммы
        int sum = num1 + num2 + num3;

        // Вывод суммы
        System.out.println("Сумма чисел: " + sum);

        // Закрытие сканера
        scanner.close();
    }
}


